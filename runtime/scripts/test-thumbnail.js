const fs = require('fs');
const path = require('path');
const os = require('os');
const { exec } = require('child_process');
const { promisify } = require('util');
const execAsync = promisify(exec);

// 错误分类
const ErrorType = {
  FILE_NOT_FOUND: 'FILE_NOT_FOUND',
  RENAME_ERROR: 'RENAME_ERROR',
  EXECUTION_ERROR: 'EXECUTION_ERROR',
  THUMBNAIL_NOT_GENERATED: 'THUMBNAIL_NOT_GENERATED',
  UNKNOWN_ERROR: 'UNKNOWN_ERROR'
};

// 查找目录下所有的 CAD 文件（.dwg 和 .dxf），全部重新生成缩略图
function findCadFiles(directories) {
  const cadFiles = [];
  let totalScanned = 0;
  
  function searchDirectory(dir) {
    try {
      const files = fs.readdirSync(dir);
      for (const file of files) {
        const fullPath = path.join(dir, file);
        const stat = fs.statSync(fullPath);
        
        if (stat.isDirectory()) {
          // 递归搜索子目录
          searchDirectory(fullPath);
        } else if (stat.isFile()) {
          totalScanned++;
          const ext = path.extname(file).toLowerCase();
          if (ext === '.dwg' || ext === '.dxf') {
            cadFiles.push(fullPath);
          }
        }
      }
    } catch (error) {
      console.error(`搜索目录时出错: ${dir}`, error.message);
    }
  }
  
  directories.forEach(dir => searchDirectory(dir));
  
  // 输出扫描统计
  console.log(`\n扫描完成:`);
  console.log(`- 总文件数: ${totalScanned}`);
  console.log(`- 图纸文件数: ${cadFiles.length}`);
  console.log(`- 待重新生成缩略图文件数: ${cadFiles.length}`);
  
  return cadFiles;
}

// 检查是否已有缩略图
function hasThumbnail(filePath) {
  const thumbnailPath = `${filePath}.jpg`;
  return fs.existsSync(thumbnailPath);
}

// 清理文件名，只处理会导致问题的特殊字符，保留中文
function cleanFileName(filePath) {
  const dir = path.dirname(filePath);
  const originalName = path.basename(filePath);
  const ext = path.extname(originalName);
  const baseName = path.basename(originalName, ext);
  
  // 只处理会导致问题的特殊字符，保留中文和其他安全字符
  // 空格替换为下划线（命令行需要）
  // 移除文件系统中不允许的字符：/ \ : * ? " < > |
  const cleanedBaseName = baseName
    .replace(/\s+/g, '_') // 空格替换为下划线
    .replace(/[\/\\:*?"<>|]/g, ''); // 只移除文件系统中不允许的字符
  
  const cleanedName = `${cleanedBaseName}${ext}`;
  const cleanedPath = path.join(dir, cleanedName);
  
  return { originalName, cleanedName, cleanedPath };
}

// 安全的重命名函数，带备份机制
function safeRename(oldPath, newPath) {
  // 检查目标路径是否存在
  if (fs.existsSync(newPath)) {
    // 生成唯一的目标路径
    const dir = path.dirname(newPath);
    const ext = path.extname(newPath);
    const base = path.basename(newPath, ext);
    let counter = 1;
    let tempPath;
    do {
      tempPath = path.join(dir, `${base}_${counter}${ext}`);
      counter++;
    } while (fs.existsSync(tempPath));
    newPath = tempPath;
  }
  
  // 重命名文件
  fs.renameSync(oldPath, newPath);
  return newPath;
}

// 为单个 CAD 文件生成缩略图
async function generateThumbnailForFile(cadFilePath, dwg2JpgPath, tempPath) {
  let finalFilePath = cadFilePath;
  let originalName = path.basename(cadFilePath);
  let needsRenameBack = false;
  let paramFilePath = null;
  let backupPath = null;



  try {
    // 检查文件是否存在
    if (!fs.existsSync(cadFilePath)) {
      return { success: false, errorType: ErrorType.FILE_NOT_FOUND, errorMessage: '文件不存在' };
    }

    // 清理文件名
    const { cleanedName, cleanedPath } = cleanFileName(cadFilePath);
    if (cleanedPath !== cadFilePath) {
      try {
        // 安全重命名
        finalFilePath = safeRename(cadFilePath, cleanedPath);
        originalName = cleanedName;
        needsRenameBack = true;
      } catch (renameError) {
        return { success: false, errorType: ErrorType.RENAME_ERROR, errorMessage: renameError.message };
      }
    }

    // 构建参数（与后端配置一致：宽度100px，高度100px，黑色背景）
    const cadFilePathForward = finalFilePath.replace(/\\/g, '/');
    const paramStr = `file=${cadFilePathForward} width=100 height=100 background_color=0x000000`;

    // 创建临时参数文件
    const toolDir = path.dirname(dwg2JpgPath);
    const paramFileName = `thumbnail_param_${Date.now()}_${Math.random().toString(36).substr(2, 9)}.json`;
    paramFilePath = path.join(tempPath, paramFileName);

    // 写入参数文件
    fs.writeFileSync(paramFilePath, paramStr, 'utf8');

    // 构建命令
    const cmd = `"${dwg2JpgPath}" cadtojpg "fileparam=${paramFilePath}"`;

    // 执行命令
    try {
      const { stdout, stderr } = await execAsync(cmd, {
        encoding: 'utf8',
        timeout: 300000, // 增加超时时间到5分钟，处理大文件
        maxBuffer: 100 * 1024 * 1024, // 增加缓冲区到100MB，处理大文件输出
        cwd: toolDir // 在tool目录下执行
      });
    } catch (execError) {
      return { success: false, errorType: ErrorType.EXECUTION_ERROR, errorMessage: execError.message };
    }

    // 计算 exe 自动生成的输出路径
    const sourceDir = path.dirname(finalFilePath);
    const sourceBaseName = path.basename(finalFilePath);
    const autoGeneratedPath = path.join(sourceDir, `${sourceBaseName}.jpg`);

    // 检查生成的缩略图
    if (fs.existsSync(autoGeneratedPath)) {
      // 如果重命名了文件，也需要重命名缩略图到原始位置
      if (needsRenameBack) {
        const originalThumbnailPath = path.join(path.dirname(cadFilePath), `${path.basename(cadFilePath)}.jpg`);
        try {
          // 安全重命名缩略图
          if (fs.existsSync(originalThumbnailPath)) {
            fs.unlinkSync(originalThumbnailPath);
          }
          fs.renameSync(autoGeneratedPath, originalThumbnailPath);
        } catch (renameError) {
          // 忽略重命名缩略图的错误，不影响主流程
        }
      }
      
      return { success: true, message: '缩略图生成成功' };
    } else {
      return { success: false, errorType: ErrorType.THUMBNAIL_NOT_GENERATED, errorMessage: '缩略图生成失败' };
    }

  } catch (error) {
    return { success: false, errorType: ErrorType.UNKNOWN_ERROR, errorMessage: error.message };
  } finally {
    // 恢复原始文件名
    if (needsRenameBack && fs.existsSync(finalFilePath)) {
      try {
        // 检查原始文件是否存在（以防万一）
        if (!fs.existsSync(cadFilePath)) {
          fs.renameSync(finalFilePath, cadFilePath);
        }
      } catch (renameError) {
        // 忽略恢复文件名的错误，不影响主流程
      }
    }
  }
}

// 显示进度条
function showProgress(current, total, duration) {
  const percentage = Math.round((current / total) * 100);
  const barLength = 50;
  const filledLength = Math.round((percentage / 100) * barLength);
  const bar = '='.repeat(filledLength) + ' '.repeat(barLength - filledLength);
  
  const avgTime = duration / current;
  const remainingTime = avgTime * (total - current);
  
  process.stdout.clearLine();
  process.stdout.cursorTo(0);
  process.stdout.write(`[${bar}] ${percentage}% (${current}/${total}) - 剩余时间: ${Math.round(remainingTime)}s`);
}

// 并发处理任务队列
async function processWithConcurrency(tasks, concurrencyLimit) {
  let successCount = 0;
  let failCount = 0;
  let skipCount = 0;
  let activeTasks = 0;
  let taskIndex = 0;
  const results = [];
  const errorStats = {};
  const startTime = Date.now();

  return new Promise((resolve) => {
    function runNextTask() {
      // 检查是否所有任务都已处理
      if (taskIndex >= tasks.length && activeTasks === 0) {
        process.stdout.write('\n');
        resolve({ successCount, failCount, skipCount, results, errorStats });
        return;
      }

      // 显示进度
      const processed = successCount + failCount + skipCount;
      if (processed > 0) {
        const duration = (Date.now() - startTime) / 1000;
        // 显示总进度，使用全局变量 totalTasks
        showProgress(processed, global.totalTasks || tasks.length, duration);
      }

      // 检查是否可以启动新任务
      while (activeTasks < concurrencyLimit && taskIndex < tasks.length) {
        const task = tasks[taskIndex];
        taskIndex++;
        activeTasks++;

        // 执行任务
        task()
          .then((result) => {
            results.push(result);
            if (result.success) {
              if (result.skipped) {
                skipCount++;
              } else {
                successCount++;
              }
            } else {
              failCount++;
              // 统计错误类型
              const errorType = result.errorType || 'UNKNOWN';
              errorStats[errorType] = (errorStats[errorType] || 0) + 1;
            }
          })
          .catch((error) => {
            failCount++;
            errorStats['PROMISE_ERROR'] = (errorStats['PROMISE_ERROR'] || 0) + 1;
            results.push({ success: false, errorType: 'PROMISE_ERROR', errorMessage: error.message });
          })
          .finally(() => {
            activeTasks--;
            // 任务完成后，尝试启动新任务
            runNextTask();
          });
      }
    }

    // 开始执行任务
    runNextTask();
  });
}

// 批量处理任务
async function processInBatches(tasks, batchSize, initialConcurrencyLimit) {
  let totalSuccess = 0;
  let totalFail = 0;
  let totalSkip = 0;
  let totalErrorStats = {};
  const totalTasks = tasks.length;
  let processedTasks = 0;

  for (let i = 0; i < tasks.length; i += batchSize) {
    const batch = tasks.slice(i, i + batchSize);
    
    console.log(`\n处理批次 ${Math.floor(i / batchSize) + 1}/${Math.ceil(tasks.length / batchSize)} (${batch.length} 个文件)`);
    
    const { successCount, failCount, skipCount, errorStats } = await processWithConcurrency(batch, initialConcurrencyLimit);
    
    totalSuccess += successCount;
    totalFail += failCount;
    totalSkip += skipCount;
    processedTasks += batch.length;
    
    // 合并错误统计
    Object.keys(errorStats).forEach(key => {
      totalErrorStats[key] = (totalErrorStats[key] || 0) + errorStats[key];
    });
  }
  
  process.stdout.write('\n');

  return { totalSuccess, totalFail, totalSkip, totalErrorStats, totalTasks };
}

// 清理临时文件目录的函数
function cleanupTempDir(tempPath) {
  try {
    if (fs.existsSync(tempPath)) {
      fs.rmSync(tempPath, { recursive: true, force: true });
      console.log('\n临时文件已清理');
    }
  } catch (cleanupError) {
    console.error('清理临时文件失败:', cleanupError.message);
  }
}

// 主函数
async function main() {
  // 配置
    const config = {
      dwg2JpgPath: path.join(__dirname, '..', 'runtime', 'windows', 'mxcad', 'tool', 'MxWebDwg2Jpg.exe'),
      tempPath: path.join(__dirname, '..', 'data', 'temp'),
      outputRootDir: path.join(__dirname, '..', 'data', 'test'),
      // 指定要处理的目录合集
      directories: [
        path.join(__dirname, '..', 'data', 'block-library'),
        path.join(__dirname, '..', 'data', 'drawing-library')
      ],
      // concurrencyLimit: 30,
      concurrencyLimit: 50, // 固定并发数，CPU核心数-2，最少6个
      batchSize: 100 // 批量大小，调大减少批次切换
    };
  
  // 注册信号处理，确保意外退出时清理临时文件
  process.on('SIGINT', () => {
    console.log('\n收到中断信号，正在清理临时文件...');
    cleanupTempDir(config.tempPath);
    process.exit(1);
  });
  
  process.on('SIGTERM', () => {
    console.log('\n收到终止信号，正在清理临时文件...');
    cleanupTempDir(config.tempPath);
    process.exit(1);
  });
  
  try {

    console.log('开始批量生成缩略图...');
    console.log('工具路径:', config.dwg2JpgPath);
    console.log('临时目录:', config.tempPath);
    console.log('处理目录:', config.directories);
    console.log('CPU核心数:', os.cpus().length);
    console.log('并发处理数:', config.concurrencyLimit);
    console.log('批量处理大小:', config.batchSize);

    // 检查工具是否存在
    if (!fs.existsSync(config.dwg2JpgPath)) {
      console.error('✗ MxWebDwg2Jpg.exe 不存在');
      return;
    }

    // 确保临时目录存在
    if (!fs.existsSync(config.tempPath)) {
      fs.mkdirSync(config.tempPath, { recursive: true });
    }

    // 查找所有 CAD 文件
    console.log('\n正在查找 CAD 文件...');
    const cadFiles = findCadFiles(config.directories);
    console.log(`找到 ${cadFiles.length} 个 CAD 文件`);

    // 构建任务队列
    console.log('\n构建任务队列...');
    const tasks = cadFiles.map(file => () => 
      generateThumbnailForFile(file, config.dwg2JpgPath, config.tempPath)
    );

    // 设置全局变量，用于显示总进度
    global.totalTasks = tasks.length;

    // 全程并发处理任务（保持最多并发数，完成一个补充一个）
    console.log('\n开始全量并发生成缩略图...');
    const startTime = Date.now();
    const { successCount: totalSuccess, failCount: totalFail, skipCount: totalSkip, errorStats: totalErrorStats } = await processWithConcurrency(tasks, config.concurrencyLimit);
    const totalTasks = tasks.length;
    const endTime = Date.now();
    const duration = (endTime - startTime) / 1000;

    // 清理临时文件目录
    cleanupTempDir(config.tempPath);

    // 输出统计结果
    console.log('\n==============================');
    console.log('批量生成缩略图完成!');
    console.log(`总文件数: ${totalTasks}`);
    console.log(`成功: ${totalSuccess}`);
    console.log(`失败: ${totalFail}`);
    console.log(`跳过: ${totalSkip}`);
    console.log(`用时: ${duration.toFixed(2)} 秒`);
    console.log(`平均速度: ${(totalTasks / duration).toFixed(2)} 文件/秒`);
    
    // 输出错误统计
    if (Object.keys(totalErrorStats).length > 0) {
      console.log('\n错误统计:');
      Object.entries(totalErrorStats).forEach(([errorType, count]) => {
        console.log(`- ${errorType}: ${count} 个`);
      });
    }
    console.log('==============================');

  } catch (error) {
    console.error('执行过程中出错:', error.message);
    if (error.stack) {
      console.error(error.stack);
    }
    
    // 清理临时文件目录
    cleanupTempDir(config.tempPath);
  }
}

// 执行主函数
main();
