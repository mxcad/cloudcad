const fs = require('fs');
const fsPromises = fs.promises;
const path = require('path');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

const config = {
  filesDataPath: path.resolve(__dirname, '../data/files'),
  toolPath: path.resolve(__dirname, '../runtime/windows/mxcad/tool/MxWebDwg2Jpg.exe'),
  tempPath: path.resolve(__dirname, '../data/temp'),
  thumbnailWidth: 100,
  thumbnailHeight: 100,
  backgroundColor: '0xFFFFFF',
  progressFile: path.join(__dirname, 'thumbnail-progress.json'),
  requestTimeout: 60000,
  maxConcurrent: 3
};

const progressStore = {
  data: {},
  
  async load() {
    if (fs.existsSync(config.progressFile)) {
      try {
        this.data = JSON.parse(await fsPromises.readFile(config.progressFile, 'utf8'));
        const doneCount = Object.keys(this.data).filter(k => this.data[k] === 'done').length;
        console.log(`已加载进度，跳过 ${doneCount} 个已完成文件`);
      } catch {
        this.data = {};
      }
    }
  },
  
  async save() {
    await fsPromises.writeFile(config.progressFile, JSON.stringify(this.data, null, 2));
  },
  
  markDone(filePath) {
    this.data[filePath] = 'done';
  },
  
  isDone(filePath) {
    return this.data[filePath] === 'done';
  },
  
  reset() {
    this.data = {};
    if (fs.existsSync(config.progressFile)) {
      fs.unlinkSync(config.progressFile);
    }
  }
};

function log(message, level = 'info') {
  console.log(`[${level.toUpperCase()}] ${message}`);
}

async function generateThumbnail(cadFilePath, outputDir) {
  if (!fs.existsSync(config.toolPath)) {
    throw new Error(`MxWebDwg2Jpg.exe 不存在: ${config.toolPath}`);
  }

  if (!fs.existsSync(cadFilePath)) {
    throw new Error(`CAD 文件不存在: ${cadFilePath}`);
  }

  await fsPromises.mkdir(outputDir, { recursive: true });

  const thumbnailPath = path.join(outputDir, 'thumbnail.jpg');

  const cadFilePathForward = cadFilePath.replace(/\\/g, '/');
  const paramStr = `file=${cadFilePathForward} width=${config.thumbnailWidth} height=${config.thumbnailHeight} background_color=${config.backgroundColor} out=${thumbnailPath}`;

  const paramFileName = `thumbnail_param_${Date.now()}_${Math.random().toString(36).substr(2, 9)}.txt`;
  const paramFilePath = path.join(config.tempPath, paramFileName);

  try {
    await fsPromises.writeFile(paramFilePath, paramStr, 'utf8');

    const cmd = `"${config.toolPath}" cadtojpg "fileparam=${paramFilePath}"`;
    const toolDir = path.dirname(config.toolPath);

    const { stdout, stderr } = await execAsync(cmd, {
      encoding: 'utf8',
      timeout: config.requestTimeout,
      maxBuffer: 10 * 1024 * 1024,
      cwd: toolDir
    });

    if (stdout) {
      log(`命令输出: ${stdout.trim()}`, 'debug');
    }
    if (stderr) {
      log(`命令错误输出: ${stderr.trim()}`, 'debug');
    }

    const sourceDir = path.dirname(cadFilePath);
    const sourceBaseName = path.basename(cadFilePath);
    const autoGeneratedPath = path.join(sourceDir, `${sourceBaseName}.jpg`);

    if (fs.existsSync(autoGeneratedPath)) {
      if (autoGeneratedPath !== thumbnailPath) {
        fs.copyFileSync(autoGeneratedPath, thumbnailPath);
        fs.unlinkSync(autoGeneratedPath);
      }
    } else if (!fs.existsSync(thumbnailPath)) {
      throw new Error('缩略图文件未生成');
    }

    const stats = fs.statSync(thumbnailPath);
    return {
      success: true,
      thumbnailPath,
      size: stats.size
    };
  } finally {
    if (fs.existsSync(paramFilePath)) {
      try {
        fs.unlinkSync(paramFilePath);
      } catch {
      }
    }
  }
}

async function findCadFiles(dirPath) {
  const cadFiles = [];
  
  async function search(currentPath) {
    const entries = await fsPromises.readdir(currentPath, { withFileTypes: true });
    
    for (const entry of entries) {
      const fullPath = path.join(currentPath, entry.name);
      
      if (entry.isDirectory()) {
        await search(fullPath);
      } else if (entry.isFile()) {
        const ext = path.extname(entry.name).toLowerCase();
        if (ext === '.dwg' || ext === '.dxf') {
          cadFiles.push(fullPath);
        }
      }
    }
  }
  
  await search(dirPath);
  return cadFiles;
}

class TaskQueue {
  constructor(maxConcurrent = 3) {
    this.maxConcurrent = maxConcurrent;
    this.running = 0;
    this.queue = [];
  }

  enqueue(task) {
    return new Promise((resolve, reject) => {
      this.queue.push({ task, resolve, reject });
      this.processQueue();
    });
  }

  processQueue() {
    while (this.running < this.maxConcurrent && this.queue.length > 0) {
      this.running++;
      const { task, resolve, reject } = this.queue.shift();

      task()
        .then(() => resolve())
        .catch((error) => reject(error))
        .finally(() => {
          this.running--;
          this.processQueue();
        });
    }
  }

  getQueueLength() {
    return this.queue.length;
  }

  getRunningCount() {
    return this.running;
  }
}

async function main() {
  try {
    console.log('\n========================================');
    console.log('          缩略图批量生成工具');
    console.log('========================================\n');

    if (!fs.existsSync(config.filesDataPath)) {
      console.error(`错误: 文件存储目录不存在: ${config.filesDataPath}`);
      process.exit(1);
    }

    if (!fs.existsSync(config.toolPath)) {
      console.error(`错误: MxWebDwg2Jpg.exe 不存在: ${config.toolPath}`);
      console.error('请确保 runtime/windows/mxcad/tool/MxWebDwg2Jpg.exe 已安装');
      process.exit(1);
    }

    await fsPromises.mkdir(config.tempPath, { recursive: true });
    await progressStore.load();

    console.log(`正在扫描目录: ${config.filesDataPath}`);
    const cadFiles = await findCadFiles(config.filesDataPath);
    console.log(`找到 ${cadFiles.length} 个 CAD 文件`);

    const pendingFiles = cadFiles.filter(f => !progressStore.isDone(f));
    console.log(`需要生成缩略图: ${pendingFiles.length} 个`);
    console.log(`已完成跳过: ${cadFiles.length - pendingFiles.length} 个\n`);

    if (pendingFiles.length === 0) {
      console.log('🎉 所有缩略图都已生成完成！');
      return;
    }

    const stats = {
      total: pendingFiles.length,
      success: 0,
      failed: 0,
      skipped: cadFiles.length - pendingFiles.length
    };

    const queue = new TaskQueue(config.maxConcurrent);
    const tasks = [];

    for (const cadFilePath of pendingFiles) {
      const task = queue.enqueue(async () => {
        const relativePath = path.relative(config.filesDataPath, cadFilePath);
        const nodeDir = path.dirname(cadFilePath);

        try {
          console.log(`处理: ${relativePath}`);
          
          const result = await generateThumbnail(cadFilePath, nodeDir);
          
          if (result.success) {
            progressStore.markDone(cadFilePath);
            await progressStore.save();
            stats.success++;
            console.log(`✅ 成功: ${relativePath} (${result.size} bytes)`);
          } else {
            stats.failed++;
            console.log(`❌ 失败: ${relativePath}`);
          }
        } catch (error) {
          stats.failed++;
          console.log(`❌ 失败: ${relativePath} - ${error.message}`);
        }
      });
      
      tasks.push(task);
    }

    await Promise.all(tasks);

    console.log('\n========================================');
    console.log('                 统计结果');
    console.log('========================================');
    console.log(`总文件数: ${cadFiles.length}`);
    console.log(`已跳过: ${stats.skipped}`);
    console.log(`成功生成: ${stats.success}`);
    console.log(`生成失败: ${stats.failed}`);
    console.log('========================================');

    if (stats.success > 0) {
      console.log('\n🎉 缩略图生成完成！');
    }
    
    if (stats.failed > 0) {
      console.log(`\n⚠️  有 ${stats.failed} 个文件生成失败，请检查日志`);
    }

  } catch (error) {
    console.error(`脚本执行失败: ${error.message}`);
    process.exit(1);
  }
}

main().catch(error => {
  console.error(`脚本执行失败: ${error.message}`);
  process.exit(1);
});